// Ejercicio Operadores aritméticos

// 3.1 Multiplicación
console.log ("10 * 5 =", 10 * 5);

// 3.2 División
console.log ("10 / 2 =", 10 / 2);

// 3.3 Resto (módulo) - lo que sobra de dividir
console.log ("Resto de 15 / 9 =", 15 % 9);

// 3.4 o = 15, con p = 10 y j = 5
let p = 10;
let j = 5;
let o = p + j; // 10 + 5 = 15
console.log ("o =", o); // 15

// 3.5 i = 50, con c = 10 y m = 5
let c = 10;
let m = 5;
let i = c * m; // 10 * 5 = 50
console.log ("i =", i); // 50